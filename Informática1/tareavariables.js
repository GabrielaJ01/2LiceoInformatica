var numero1 , numero2 , suma , resta , multiplicación , división
numero1 = prompt("Ingrese el primer número");
numero2 = prompt ("Ingrese el segundo número");
suma = (parseInt(numero1)+parseInt(numero2));
resta = (parseInt(numero1)-parseInt(numero2));
multiplicación = (parseInt(numero1)*parseInt(numero2));
división = (parseInt(numero1)/parseInt(numero2));
console.log (`La suma es: ${suma}`)
console.log (`La resta es: ${resta}`)
console.log (`La multiplicación es: ${multiplicación}`)
console.log (`La división es: ${división}`)